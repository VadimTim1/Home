package home_work_1;

public class home_work_ex_3 {
    public static void main(String[] args) {

        double power = Math.pow(5, 2);
        System.out.println(("5 в квадрате:" + power));

        double sqrt = Math.sqrt(5);
        System.out.println("Квадратный корень из 5: " + sqrt);

        int rounded = Math.round(5.7f);
        System.out.println("Округленное значение 5.7: " + rounded);

    }
}

